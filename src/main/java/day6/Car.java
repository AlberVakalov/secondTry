package day6;

public class Car {
    public String model;
    public String color;
    public int ageCar;

    public void setColor(String color1) {
        color = color1;
    }

    public String getColor() {
        return color;
    }

    public void setModel(String model1) {
        model = model1;
    }

    public String getModel() {
        return model;
    }

    public void setAgeCar(int age){
        ageCar = age;
    }

    public int getAgeCar(){
        return ageCar;
    }

    public int yearDifference(int i) {
        return i;
    }
}
