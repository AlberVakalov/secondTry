package day6;

public class Car {
    private String model;
    private String color;
    private int ageCar;

    public void setColor(String color1) {
        color = color1;
    }

    public String getColor() {
        return color;
    }

    public void setModel(String model1) {
        model = model1;
    }

    public String getModel() {
        return model;
    }

    public void setAgeCar(int age){
        ageCar = age;
    }

    public int getAgeCar(){
        return ageCar;
    }

}
